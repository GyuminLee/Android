public class MainActivity extends ActionBarActivity {

	@Override
	protected void onCreate(Bundle savedInstanceState) {
		super.onCreate(savedInstanceState);
		setContentView(R.layout.activity_main);

		if (savedInstanceState == null) {
			getSupportFragmentManager().beginTransaction()
					.add(R.id.container, new PlaceholderFragment()).commit();
		}
	}

	@Override
	public boolean onCreateOptionsMenu(Menu menu) {

		// Inflate the menu; this adds items to the action bar if it is present.
		getMenuInflater().inflate(R.menu.main, menu);
		return true;
	}

	@Override
	public boolean onOptionsItemSelected(MenuItem item) {
		// Handle action bar item clicks here. The action bar will
		// automatically handle clicks on the Home/Up button, so long
		// as you specify a parent activity in AndroidManifest.xml.
		int id = item.getItemId();
		if (id == R.id.action_settings) {
			return true;
		}
		return super.onOptionsItemSelected(item);
	}

	/**
	 * A placeholder fragment containing a simple view.
	 */
	public static class PlaceholderFragment extends Fragment {

		public PlaceholderFragment() {
		}

		TextView messageView;

		@Override
		public View onCreateView(LayoutInflater inflater, ViewGroup container,
				Bundle savedInstanceState) {
			View rootView = inflater.inflate(R.layout.fragment_main, container,
					false);
			messageView = (TextView) rootView.findViewById(R.id.messageView);
			Button btn = (Button) rootView.findViewById(R.id.btnGetGoogle);
			btn.setOnClickListener(new View.OnClickListener() {

				@Override
				public void onClick(View v) {
					MyNetworkFragment f = new MyNetworkFragment();
					Bundle b = new Bundle();
					b.putString(MyNetworkFragment.PARAM_URL, "http://www.google.com");
					f.setArguments(b);
					f.setOnResultListener(new MyNetworkFragment.OnResultListener() {
						
						@Override
						public void onResult(String result) {
							messageView.setText(result);
						}
					});
					
					f.show(getChildFragmentManager(), "dialog");
				}
			});
			return rootView;
		}
	}

}
