public class MP4DemuxerException extends Exception {

	public MP4DemuxerException() {
		super();
	}

	public MP4DemuxerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MP4DemuxerException(String arg0) {
		super(arg0);
	}

	public MP4DemuxerException(Throwable arg0) {
		super(arg0);
	}

}
